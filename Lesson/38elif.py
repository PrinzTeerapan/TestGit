score = float(input("How many your score: "))
if score >= 80:
    print("You got an A.")
elif score >= 75:
    print("You got a B+.")
elif score >= 70:
    print("You got a B.")
elif score >= 65:
    print("You got a C+.")
elif score >= 60:
    print("You got a C.")
elif score >= 55:
    print("You got a D+.")
elif score >= 50:
    print("You got a D.")
else:
    print("You got an F.")